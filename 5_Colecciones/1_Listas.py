# Listas en python, tambien conocidos como un array
''' 
    Para crear una lista se utiliza corchetes []
 '''

nombres = ["Juan", "Karla", "Ricardo", "María"]
print(nombres)

#Mostrar el tamaño de la lista o del array
print(len(nombres))

# Mostrando el elemento 2 
print(nombres[2])

# Navegacion inversa
print(nombres[-1])
print(nombres[-2])

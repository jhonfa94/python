# Creando funcion sumar
def sumar(a,b):
    return a + b

# Creando funcion restar
def restar(a,b):
    return a - b

# Creando funcion de multiplicar
def multiplicar(a,b):
    return a * b

# Creando funcion de dividir
def dividir(a,b):
    return a / b

